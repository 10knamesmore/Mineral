//! 子格块(1/8)字符:把 `0..=8` 的覆盖度映射成 Unicode 八分块,做**子单元格平滑填充**。
//!
//! 水平 [`left_eighth`](进度条 / 横向铺展)、垂直 [`lower_eighth`](频谱 / 浮层弹出),
//! 被浮层动画、下载进度条等多处复用 —— 单独成模块避免散落在某个组件里被跨模块借用。

/// 左对齐左八分块:`n/8` 宽(`n` ∈ `1..=8`,`8` → `█`,`0` 调用方自行不画)。
///
/// # Params:
///   - `n`: 覆盖的八分之几(`>=8` 视作满格)
///
/// # Return:
///   对应的左八分块字符。
pub(crate) fn left_eighth(n: u32) -> &'static str {
    match n {
        1 => "▏",
        2 => "▎",
        3 => "▍",
        4 => "▌",
        5 => "▋",
        6 => "▊",
        7 => "▉",
        _ => "█",
    }
}

/// 底对齐下八分块:`n/8` 高(`n` ∈ `1..=8`,`8` → `█`,`0` 调用方自行不画)。
///
/// # Params:
///   - `n`: 覆盖的八分之几(`>=8` 视作满格)
///
/// # Return:
///   对应的下八分块字符。
pub(crate) fn lower_eighth(n: u32) -> &'static str {
    match n {
        1 => "▁",
        2 => "▂",
        3 => "▃",
        4 => "▄",
        5 => "▅",
        6 => "▆",
        7 => "▇",
        _ => "█",
    }
}
